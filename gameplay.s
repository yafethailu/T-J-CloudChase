///// BitBlit (pmap, x, y)
BitBlit:
	push {r4,r5,r6,r7,r8,r9,r10,r11,lr}
	//ro - pixel ptr
	ldrh r1, [r0, #XPOS]
	ldrh r2, [r0, #YPOS]
	
	ldrh r4,[r0, #PIXMAP_WIDTH]		//r1 - x val
	ldrh r5,  [r0, #PIXMAP_HEIGHT]	//r2 - y val
	// calculate starting address values
	asr r6,r4,#1	//divide width by 2,
	asr r7,r5,#1	//divide height by 2,
	
	/// to find end points of the sprite (x end ,y end)
	add r8, r1, r6		// to find XEND
	strh r8, [r0, #XEND]
	add r9, r2, r7		// to find YEND
	strh r9, [r0, #YEND]
	mov r8, #0
	mov r9, #0
	
	sub r6, r1, r6		 //and subtract from x coordinate, now r6 has starting x value
	sub r7, r2, r7		 //and subtrct form y value, now r7 has starting y value
	
	strh r6, [r0, #XSTART]		//store staring x value in sprite.xstart
	strh r7, [r0, #YSTART]		//store staring y value in sprite.xstart
	
	// to load pixel map dimension values
	// we'll do two loops, each with a counter
	// one loop for width storing and comparision
	//another for height
	// loop controlled by cmp counter with width or (height)
	
/*-----------to check starting address with bounds---------------*/
	mov r1, #0
	mov r2, #0
	
	cmp r6, #0	// for negative starting address
	bge check1
	rsb r1, r6, #0		//save magnitude of x coordinate
	//mov r6, #0
check1:
	cmp r7, #0		// for negative starting address
	bge pass
	rsb r2, r7, #0	//copy reversed value (magnitude of y) to r2
	mov r7, #0
pass:
	ldr r8, =319	//max coordinate x val
	cmp r6, r8
	bgt exitB
	mov r9, #239
	cmp r7, r9
	bgt exitB
	
pass2:
	// now we have a valid (xstart,ystart) = @ (r6,r7)
	// next, we calculate address with offset
	//(x,y) = 0xc8000000 + ZEXT(32, { y<7:0>, x<8:0>, 0<0>})
	mov r8, #0
	ldr r8, =PIX_BUFFER   // Load base address of PIX_BUFFER into r8
    lsl r6, r6, #1        // Multiply x by 2 (each pixel occupies 2 bytes)
    lsl r7, r7, #10       // Multiply y by 1024 (320 * 2 * 4) bytes
    add r8, r8, r6        // Add the x offset (in bytes) to r8
    add r8, r8, r7       // Add the y offset (in bytes) to r8
	
	ldr r11, =PIXMAP_PIXELDATA		//load ptr to pixel pixmap data
	
	mov r9, #0		//height counter init
/*---------clear additional registers for later use------------*/
	mov r6, #0
	mov r7, #0
	
loop1:		// compares with height and exits if counter is greater than height
	cmp r9, r5	//compare the counter with length
	bge exitB
	mov r10, #0		//width counter init
	
loop2:		// compare with width r6 and draw
	cmp r10, r4
	bge exitA
	
	add r6, r8, r10, lsl #1  // Calculate column offset (multiply by 2 for each pixel)
    add r6, r6, r9, lsl #10  // Calculate row offset (multiply by 320 for each row)
	
	ldrh r7, [r0, r11]

/*------after loading out color value, we check if it has a transparency flag----*/
	ldrh r3, [r0, #PIXMAP_TRANSPARENCY]	// transparency value
	cmp r7,r3
	add r11,r11, #2
	add r10, r10, #1	//increment counter
	beq loop2		// leave without drawing if not transparency value
	
	cmp r1, #0		//if the difference between 0 and the negative address is reached, draw otherwise, loop back
	bne loopX
	b loopP
loopX:
	sub r1, r1, #1		// subtract the magnitude by 10
	b loop2		// loop back
loopP:
	strh r7, [r6]		//draw the pixels at the address
	b loop2
exitA:
	add r9,r9, #1
	b loop1
exitB:
	pop {r4,r5,r6,r7,r8,r9,r10,r11,pc}

