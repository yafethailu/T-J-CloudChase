DrawNum:
	// Draws a number (accessed through r2) at (r0, r1)
	// r0 - x 
	// r1 - y
	// r2 - pointer for number (signed, can be negative or positive)
	// prologue
	push {r4, r5, r6, r7,r8, r9,r10,r11,lr}	// callee save these registers to store them in the stack
	mov r5, r0	// for x column
	mov r6, r1	// for y row
		/// checking for address validity
		cmp r5, #WIDTH		// if the address is out of horizontal bound, exit the loop and halt execution
		bge exitDN
		cmp r6, #HEIGHT		// if the address is out of vertical bound, exit the loop and halt executio
		bge exitDN
		bl CalcLoc			// branch to CalcLoc to calculate offset and return valid address
		mov r4, r0		// r4 now holds a valid address to store our number after converting it to a string
		
		
		mov r5, #0	//clear for later use
		mov r8, #0	//clear for later use
		ldrsb r7, [r2], #1	//load the signed value from r2 and increment r2
		cmp r7, #0		// compare the value with 0
		bge Positive   // Branch if not negative
		mov r10, #45	// if negative, we move '-' ASCII code to r10
		strb r10, [r4], #1	// store '-' directly in our location from CalcLoc step
		rsb r7,r7, #0		// convert to positive number
	
	Positive:
		mov r11, r7		//move converted number to r11
		cmp r2, #0		// compare r2 for null pointer ( end of string)
		beq exitDN		// exit if end is reached
		mov r7, #0		// r7 to store quotient
		mov r8, #0		// r8 to store reminders
		mov r9, #0	

	loopn:
		cmp r11, #10         // Check if the digit is less than 10
    	blt singleDigit // If less than 10, directly store ASCII representation
		sub r11, r11, #10      //subtract quotient multiplied by 2 by remainder
		add r7, r7, #1	//quotient in r7
		cmp r11, #10		// if difference is greater than 10, loop again
		bge loopn

		mov r8, r11		//store remainder in r8
		push {r8}		// push remainder into a stack
		add r5, r5, #1		// to keep track of push
		cmp r7, #10		// compare quotient with 10
		mov r11, r7		// move new quotient to r11
		mov r7, #0		//clear r7
    	bge loopn		// loop back if new quotient is greater than 10
		mov r8, r11		// push last remainder to r8 and then to the stack
		push {r8}
		add r5, r5, #1		// r5 to keep track of number of stack elements
	loopm:
		pop {r8}		// pop the last-in digit in the stack
		add r9, r8, #48    // Convert remainder to ASCII representation
		strb r9, [r4], #1	// store converted number at the location
		sub r5,r5,#1		// decrease stack element counter by 1 for each iteration
		cmp r5, #0		//compare the counter to see if all elements have been popped
		bne loopm		// loop back if not
		b exitDN		// exit, if all elements have been popped
		
	singleDigit:
		add r9, r11, #48    // Convert single-digit number to ASCII representation
    	strb r9, [r4], #1   // Store the ASCII character
		b exitDN
		
	exitDN:
		pop {r4,r5,r6, r7,r8,r9,r10,r11,pc}

