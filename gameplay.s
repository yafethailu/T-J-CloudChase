/*------ DrawStr------------*/
CalcLoc:	
		push {r4,r7,r8,lr}
		// r4 ‚Üê {r6<5:0>, r5<6:0>}	
		ldr r4, =TEXT_BUFFER	//add the base address of the character buffer
		and r7, r6, #0x3f	// extract last 6 bits of y
		and r8, r5, #0x7f	// extract last 7 bits of x and concatenate with r4
		add r4, r4, r8		// add it to r4
		add r4,r4, r7, lsl #7		// add the y value with the appropriate shift to r4
		mov r0, r4				// move our return value to r0
		pop {r4, r7,r8, pc}

DrawStr:
	// Draws a null terminated string (accessed through r2) at (r0, r1)
	// r0 - x 
	// r1 - y
	// r2 - pointer for null terminated string
	// prologue
	push {r4, r5, r6, r7,r8,r9, lr}	// callee save these registers to store them in the stack
	mov r5, r0	// for x column
	mov r6, r1	// for y row
	mov r7, r2	// for r2 (pointer for string)
	
	/// to check if character is within screen boundaries
		cmp r5, #WIDTH		// if the address is out of horizontal bound, exit the loop and halt execution
		bge exitDS
		cmp r6, #HEIGHT		// if the address is out of vertical bound, exit the loop and halt executio
		bge exitDS
		bl CalcLoc
		mov r4, r0
	
	LoopDs:
		
		ldrb r8, [r7], #1		//load current chracter from string in r7
				
		cmp r8, #0	// to check if null pointer is reached
		beq exitDS	// if null pointer is reahced, we exit the loop
		//add r7, r7, #1	// increment by 1 so it points to the next chracter
		
		strb r8, [r4], #1		// store the character at address r4
		b LoopDs
		

	exitDS:
		pop {r4,r5,r6,r7,r8,r9,pc}

	// Our goal is to Iterate over each character in the input string until
	// a null terminator is encountered.
